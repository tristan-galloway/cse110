num_1 = 12
num_2 = 45

# if you see a colon, the next line must be indented
# space matters in python, indentation matters
if num_1 == num_2:
    # python doesn't like blank space, pass lets it know to move on.
    pass
elif num_1 > num_2:
    print("Your first number is larger than the second.")
else:
    print("Your first number is smaller than the second.")